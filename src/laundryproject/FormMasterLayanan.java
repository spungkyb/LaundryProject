    private void idauto()
    {
        konek.getkoneksi();
        try {
            String sql="select max(right(id_lyn,3)) as no from layanan";
            konek.rs=konek.st.executeQuery(sql);
            while(konek.rs.next()){
                
                if(konek.rs.first()==false){
                    txtidlayanan.setText("L-001");
                }
                else{
                    konek.rs.last();
                    int a = konek.rs.getInt(1)+1;
                    String kode1 = String.valueOf(a);
                    int kode_leng=kode1.length();
                    
                    for(int i=0;i<3-kode_leng;i++){
                        kode1="0"+kode1;
                    }
                txtidlayanan.setText("L-"+kode1);
                }
                
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Kesalahan ID = "+e);
        }
    }

